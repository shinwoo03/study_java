
public class IFEx06 {
public static void main(String[] args) {
	//정수 : 양의정수, 음의정수, 0으로 분류.
	//어떤수가 양의정수, 음의정수, 0인지 출력하기
	
	int num = 8;
	if (num > 0) {
		System.out.println(num + "은(는) 양의정수");
	} else if (num < 0) {
		System.out.println(num + "은(는) 음의정수");
	} else {
		System.out.println("0");
	}
	
	
	
}
}
