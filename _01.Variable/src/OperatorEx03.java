
public class OperatorEx03 {
	public static void main(String[] args) {
		//비교연산자: >, <, >=, <=, ==, !=(아니다) --> 비교연산한 결과는 true/false
		boolean result = 10 > 5 ; //맞습니다: true
		System.out.println( result );
		
		result = 10 == 5 ; //맞지않습니다: false
		System.out.println( result );
		
		result = 10 != 5; //맞습니다: true
		System.out.println( result );
		
		//
		
		
	}
}
